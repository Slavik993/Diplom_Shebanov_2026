    /// <summary>
    /// Генерирует промпт для LLM на основе выбранного кейса
    /// </summary>
    public static string BuildScenarioPrompt(AdaptationCase c, string playerCulture = "")
    {
        string cultureNote = string.IsNullOrEmpty(playerCulture) 
            ? "" 
            : $"\nКультурный контекст игрока: {playerCulture}. Учитывай это при диалоге.";

        return $@"Ты — {c.NPCRole} в Московском университете имени С.Ю. Витте (МУИВ).
Твоё настроение: {c.Emotion}.{cultureNote}

СИТУАЦИЯ (внутренний контекст, не раскрывай напрямую):
{c.RealProblem}

УЧЕБНАЯ ЦЕЛЬ сценария: {c.LearningGoal}

СЦЕНАРИЙ для студента:
{c.GameScenario}

ПРАВИЛА ОБЩЕНИЯ:
1. Говори от первого лица как персонаж (НПС).
2. Не раскрывай 'учебную цель' напрямую — пусть студент сам придёт к пониманию через диалог.
3. Если студент агрессивен — мягко, но твёрдо объясни свою позицию.
4. Если студент задаёт правильные вопросы — поощряй и давай полезную информацию.
5. Если студент предлагает компромисс — поддержи его и похвали.
6. Используй конкретные детали МУИВ: декан, деканат, общежитие на ул. XXX, столовая на 2 этаже.
7. Отвечай на русском языке, простыми предложениями (уровень B1-B2).
8. Ответ — 2-4 предложения максимум.";
    }

    /// <summary>
    /// Возвращает случайный кейс
    /// </summary>
    public static AdaptationCase GetRandomCase()
    {
        var cases = GetAllCases();
        return cases[Random.Range(0, cases.Count)];
    }

    /// <summary>
    /// Возвращает кейс по ID (1-20)
    /// </summary>
    public static AdaptationCase GetCaseById(int id)
    {
        return GetAllCases().Find(c => c.Id == id);
    }
}

/// <summary>
/// Структура адаптационного кейса
/// </summary>
[System.Serializable]
public class AdaptationCase
{
    public int Id;
    public string ShortTitle;       // Короткое название для UI
    public string RealProblem;      // Реальная проблема (для дипломной работы)
    public string WhyUnsolvable;    // Почему эта проблема "нерешаема" традиционными методами
    public string LearningGoal;     // Учебная цель сценария
    public string GameScenario;     // Игровой сценарий для студента
    public string NPCRole;          // Роль NPC в сценарии
    public string Emotion;          // Эмоциональное состояние NPC
}
