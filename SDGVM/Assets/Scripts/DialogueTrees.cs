using System.Collections.Generic;

/// <summary>
/// Статический класс со всеми готовыми диалоговыми деревьями.
/// Каждый метод возвращает полное дерево для одного проблемного кейса.
/// </summary>
public static class DialogueTrees
{
    private static Dictionary<int, DialogueTree> _cache;

    /// <summary>
    /// Возвращает дерево для заданного кейса (или null)
    /// </summary>
    public static DialogueTree GetTree(int caseId)
    {
        if (_cache == null) BuildCache();
        return _cache.ContainsKey(caseId) ? _cache[caseId] : null;
    }

    /// <summary>
    /// Возвращает список ID кейсов, для которых есть деревья
    /// </summary>
    public static List<int> GetAvailableCaseIds()
    {
        if (_cache == null) BuildCache();
        return new List<int>(_cache.Keys);
    }

    private static void BuildCache()
    {
        _cache = new Dictionary<int, DialogueTree>
        {
            { 2, BuildCase2_Payment() },
            { 6, BuildCase6_Racism() },
            { 11, BuildCase11_Procrastination() },
            { 15, BuildCase15_Roommate() },
            { 20, BuildCase20_Career() }
        };
    }

